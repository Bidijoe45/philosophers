#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;
	size_t	str_len;

	str_len = ft_strlen(s);
	str = (char *)s;
	i = str_len;
	while (i >= 0)
	{
		if (str[i] == (char)c)
		{
			return (&str[i]);
		}
		i--;
	}
	return (0);
}
