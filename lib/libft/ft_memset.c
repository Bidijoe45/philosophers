#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*z;

	z = b;
	i = 0;
	while (i < len)
	{
		*z = c;
		z++;
		i++;
	}
	return (b);
}
